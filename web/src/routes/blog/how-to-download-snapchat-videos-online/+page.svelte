                <p>It's important to set expectations here. Snapchat has two types of content with very different accessibility:</p>
                <ul class="tips-list">
                    <li><strong>Public Spotlight videos</strong> — these are shared publicly on Snapchat's Spotlight feed and accessible at <code>spotlight.snapchat.com</code>. SnapSave can download these.</li>
