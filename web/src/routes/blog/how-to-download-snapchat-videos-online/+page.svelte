                <h2>Common problems and fixes</h2>
                <dl class="issues-list">
                    <dt>SnapSave says the link isn't supported</dt>
                    <dd>Check that your link starts with <code>spotlight.snapchat.com</code> or <code>www.snapchat.com/spotlight</code>. Links to private profiles or chat snaps won't work.</dd>

                    <dt>The Spotlight video has expired</dt>
                    <dd>Some Spotlight videos are removed by the creator or by Snapchat's moderation. If the video no longer plays in your browser, it can't be downloaded either.</dd>

                    <dt>The download is slow</dt>
                    <dd>Snapchat videos are served through Snapchat's CDN, which may throttle connections from outside the US. Try downloading on a different network or at a different time of day.</dd>
