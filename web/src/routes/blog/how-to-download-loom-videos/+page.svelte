                    <dt>SnapSave says the video isn't available</dt>
                    <dd>The video is likely password-protected or requires a Loom account to view. Open the link in incognito mode — if it asks you to sign in, SnapSave can't access it.</dd>

                    <dt>The download starts but gets cut off</dt>
                    <dd>Loom videos can be long and large. Make sure you have enough storage, and try on a stable WiFi connection rather than mobile data.</dd>

                    <dt>The link format doesn't seem right</dt>
                    <dd>Loom share links follow the format <code>loom.com/share/...</code>. If your URL looks different (e.g., an embed URL), try navigating to the original share page and copying from there.</dd>
